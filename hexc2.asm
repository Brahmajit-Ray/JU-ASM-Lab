.MODEL SMALL

.STACK 100H

.DATA
VAR1 DB "INPUT NUMBER:",10,13,"$"
VAR2 DB "HEX:$",10,13

.CODE

MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	
	MOV DX,OFFSET VAR1
	MOV AH,09H
	INT 21H
	
	MOV CX,10
	MOV BX,0
	
INPUT:
	MOV AH,01H
	INT 21H
	
	CMP AL,13
	JE TER
	
	SUB AL,30H
	MOV AH,0
	PUSH AX
	
	MOV AX,BX
	MUL CX
	
	MOV BX,AX
	POP AX
	ADD BX,AX
	JMP INPUT
	
TER:
	MOV AX,BX
	MOV CX,16
	MOV BX,0
	
HEX:
	DIV CX
	PUSH DX
	MOV DX,0
	INC BL 
	CMP AX,0
	JNE HEX

	
PRINT:
	POP AX
	CMP AX,9
	JG L2
	
	L1:
		ADD AL,48
		MOV DL,AL
		MOV AH,02H
		INT 21H
		
		INC BH
		CMP BH,BL 
		JNE PRINT
		JMP L3
		
	L2:
		ADD AL,55
		MOV DL,AL
		MOV AH,02H
		INT 21H
		
		INC BH
		CMP BH,BL
		JNE PRINT
		
	L3:
		MOV AH,4CH
		INT 21H

MAIN ENDP
END MAIN 