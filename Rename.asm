.MODEL SMALL
.STACK 100H

.DATA

FILE1 DB "old.txt",0
FILE2 DB "new.txt",0
FAIL DB "Failed$"
PASS DB "Success$"

.CODE

	MAIN PROC
	MOV AX,@DATA
	MOV DS,AX
	MOV ES,AX

	MOV DX,OFFSET FILE1
	MOV DI,OFFSET FILE2

	MOV AH,56H
	INT 21H

	JC ERROR
	
	MOV DX,OFFSET PASS
	MOV AH,09H
	INT 21H
	jmp end1

	ERROR:
	
	MOV DX,OFFSET FAIL
	MOV AH,09H
	INT 21H	
	end1:
	MOV AH,4CH
	INT 21H
	
MAIN ENDP
END MAIN