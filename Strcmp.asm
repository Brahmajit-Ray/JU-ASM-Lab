.MODEL SMALL
.STACK 100H

.DATA

VAR1 DB "ENTER STRING1:","$"
VAR2 DB "ENTER STRING2:","$"

STR1 DB 50 DUP('$')
STR2 DB 50 DUP('$')

VAR3 DB "EQUAL",10,13,"$"
VAR4 DB "NOT EQUAL",10,13,"$"

LEN1 DW ?
LEN2 DW ?

.CODE

MAIN PROC
	MOV AX,@DATA
	MOV DS,AX 

	MOV DX,OFFSET VAR1
	MOV AH,09H
	INT 21H
	
	MOV CX,0
	MOV SI,OFFSET STR1
	CALL TEXT
	
	MOV LEN1,CX
	
	MOV DX,OFFSET VAR2
	MOV AH,09H
	INT 21H
	
	MOV CX,0
	MOV SI,OFFSET STR2 
	CALL TEXT2
	
	MOV LEN2,CX
	
	MOV CX,0
	MOV SI,OFFSET STR1
	MOV DI,OFFSET STR2
	
	COMPARE:
	
		MOV AX,LEN1
		MOV BX,LEN2
		CMP AX,BX
		JNE KILL
		
		MOV CX,LEN1 
		L1:
			MOV AL,[SI]
			MOV BL,[DI]
			
			CMP AL,BL
			JNE KILL
			INC SI
			INC DI 
			LOOP L1	

		MOV DX,OFFSET VAR3
		MOV AH,09H
		INT 21H
		
		MOV AH,4CH
		INT 21H
		
		
		
	KILL:
		MOV DX,OFFSET VAR4
		MOV AH,09H
		INT 21H
		
		MOV AH,4CH
		INT 21H
		
	MAIN ENDP 
	
TEXT PROC

    INPUT:
		MOV AH,01H
		INT 21H

		CMP AL,13
		JE TER
		
		MOV [SI],AL		
		INC SI
		INC CX
		JMP INPUT
	
	TER:
		MOV [SI],'$ '
		RET
		
	
TEXT ENDP

TEXT2 PROC

    INPUT2:
		MOV AH,01H
		INT 21H

		CMP AL,13
		JE TER2
		
		MOV [SI],AL		
		INC SI
		INC CX
		JMP INPUT2
	
	TER2:
		MOV [SI],'$ '
		RET
		
	
TEXT2 ENDP
	
END MAIN
	
	
	

