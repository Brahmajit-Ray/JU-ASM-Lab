.MODEL SMALL
.STACK 100H

.DATA

VAR1 DB "INPUT NUMBER:$",10,13
VAR2 DB "BINARY:$",10,13
VAR3 DB "HEX:$",10,13

.CODE

MAIN PROC
	MOV AX,@DATA
	MOV DS,AX 

	MOV DX,OFFSET VAR1
	MOV AH,09H
	INT 21H
	
	CALL BIN
	;CALL HEX

	MOV AH,4CH
	INT 21H

MAIN ENDP

BIN PROC
    MOV CX,0
	MOV AH,0
	
	MOV AH,01H
	INT 21H
	
	CONVERT:
		MOV BL,02
		SUB AL,30H
		
		CMP AX,0
		JE PRINT
		
		DIV BL
		PUSH DX
		INC CX
		JMP CONVERT
		
	PRINT:
		MOV DX,OFFSET VAR2
		MOV AH,09H
		INT 21H
		
		L1:
			POP DX
			ADD DX,30H
			MOV AH,02H
		    INT 21H
		LOOP L1
		
		RET
		
BIN ENDP

END MAIN
			
		
		
		
		
		
		
		
	
