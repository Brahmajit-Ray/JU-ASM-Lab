.MODEL SMALL
.STACK 100H

.DATA

VAR1 DB "Input text:$",13
VAR2 DB 50 DUP('$')

.CODE

MAIN PROC

	MOV AX,@DATA
	MOV DS,AX

	MOV DX,OFFSET VAR1
	MOV AH,09H
	INT 21H

	MOV SI,OFFSET VAR2
	CALL TEXT


	MOV AH,4CH
	INT 21H

	MAIN ENDP

TEXT PROC

    INPUT:
		MOV AH,01H
		INT 21H

		CMP AL,13
		JE TER
		
		MOV [SI],AL		
		INC SI
		JMP INPUT
	
	TER:
		MOV [SI],'$ '
		MOV BX,OFFSET VAR2
		MOV DX,BX
		MOV AH,09H
		INT 21H
		RET
		
	
TEXT ENDP

END MAIN